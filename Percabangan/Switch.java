package Percabangan;
public class Switch {
    public static void main(String[] args) {
        char nilai = 'B';
        switch(nilai){
            case 'A':
            System.out.println("wow");
            break;
            case 'B':
            case 'C':
            System.out.println("wiw");
            break;
            case 'D':
            System.out.println("wew");
            break;
            default:
            System.out.println("wuw");
        }
    }
}
